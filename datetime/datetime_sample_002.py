# 任意の日時のdatetimeオブジェクトを作成 (datetime.datetime)

import datetime

dt = datetime.datetime (2023 , 1 , 1 , 23 , 59 , 59 , 123456)

print(dt)
print(dt.year)
print(dt.month)
print(dt.day)
print(dt.hour)
print(dt.minute)
print(dt.second)
print(dt.microsecond)


# 実行結果
# 2023-01-01 23:59:59.123456
# 2023
# 1
# 1
# 23
# 59
# 59
# 123456
